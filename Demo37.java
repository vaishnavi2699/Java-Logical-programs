//Instance variable
public class Demo37 
{
	int a;
	float b;
	char c;
	boolean d;
	public void display()
	{
		System.out.println("Integer value is: "+a);
		System.out.println("float value is: "+b);
		System.out.println("double value is: "+c);
		System.out.println("char value is: "+d);
	}
	public static void main(String[] args)
	{
	  Demo37 d=new Demo37();
	  d.display();
	  
	}

}
